	<h2>Teknik agroindustri</h2>
	<p>
		Agroindustri adalah kegiatan yang memanfaatkan hasil pertanian sebagai bahan baku, merancang dan menyediakan peralatan serta jasa untuk kegiatan tersebut. Secara eksplisit pengertian Agroindustri pertama kali diungkapkan oleh Austin (1981) yaitu perusahaan yang memproses bahan nabati (yang berasal dari tanaman) atau hewani (yang dihasilkan oleh hewan). Proses yang digunakan mencakup pengubahan dan pengawetan melalui perlakuan fisik atau kimiawi, penyimpanan, pengemasan dan distribusi. Produk Agroindustri ini dapat merupakan produk akhir yang siap dikonsumsi ataupun sebagai produk bahan baku industri lainnya. Agroindustri merupakan bagian dari kompleks industri pertanian sejak produksi bahan pertanian primer, industri pengolahan atau transformasi sampai penggunaannya oleh konsumen. Agroindustri merupakan kegiatan yang saling berhubungan (interelasi) produksi, pengolahan, pengangkutan, penyimpanan, pendanaan, pemasaran dan distribusi produk pertanian. Dari pandangan para pakar sosial ekonomi, agroindustri (pengolahan hasil pertanian) merupakan bagian dari lima subsistem agribisnis yang disepakati, yaitu subsistem penyediaan sarana produksi dan peralatan. usaha tani, pengolahan hasil, pemasaran, sarana dan pembinaan. Agroindustri dengan demikian mencakup Industri Pengolahan Hasil Pertanian (IPHP), Industri Peralatan Dan Mesin Pertanian (IPMP) dan Industri Jasa Sektor Pertanian (IJSP).
